    public double raiz_cuadrada(float numero){
       float aux;
       
       float grado= numero/2;
       
         do{
            aux=grado;
            grado = (aux + (numero/aux)) / 2;
            
         }while((aux-grado)!=0);   
       
       return grado;
  
   }
    
    
