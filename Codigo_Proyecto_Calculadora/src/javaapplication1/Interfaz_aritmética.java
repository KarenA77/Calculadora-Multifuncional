    public String sinpunto(float resultado){ //Para eliminar el punto decimal por el flotante, sin afectar las operaciones que puedan contenerlo. 
        String retornar="";
  
        retornar=Float.toString(resultado);
        
        if(resultado%1==0){
        retornar=retornar.substring(0,retornar.length()-2);
        }
        
        return retornar;
    }
    
 
   

     

    
    
