    private void radianescalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_radianescalActionPerformed
       //boton para calcular radianes
        this.x=Double.parseDouble(this.RadianesSeno.getText()); 
        
        
        for(int i=0; i<=10; i++){
        
            numerador=1;
           for(int a=0; a<2*i+1; a++){
              numerador=numerador*x;
             
           }
           factorial=1;
           for(int a=1; a<=2*i+1; a++){
              factorial=factorial*a; 

           }
           
           if(i%2==0){
             alternante=1;
           
           }else{
             alternante=-1;
           } 
           
           
           this.seno1= seno1+(numerador/factorial)*alternante;
           
           this.resultado1= Math.round(this.seno1*100);
        }
           this.resultrad.setText(sinpunto(this.resultado1/100));
    }//GEN-LAST:event_radianescalActionPerformed

    
    
    
    private void gradoscalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_gradoscalcActionPerformed
        //boton para calcularlo en grados, haciendo el calculo en radianes y convirtiendolo en grados        
         this.x=Double.parseDouble(this.GradosSeno1.getText());
         
         grad=x*0.0175;
         
             for(int i=0; i<=10; i++){
        
                 numerador=1;
                 for(int a=0; a<2*i+1; a++){
                     numerador=numerador*grad;
             }
                  
                 factorial=1;
                 for(int a=1; a<=2*i+1; a++){
                     factorial=factorial*a; 

             }
           
             if(i%2==0){
             alternante=1;
           
                 }else{
                  alternante=-1;
                 } 
             
                  this.seno2= seno2 + (numerador/factorial)*alternante;
                  this.resultado2= Math.round(this.seno2*100);
             }
                  this.resultgrad.setText(sinpunto(this.resultado2/100));
        
           
    }//GEN-LAST:event_gradoscalcActionPerformed

