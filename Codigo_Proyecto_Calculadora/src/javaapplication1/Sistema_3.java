    public double det3(double a11, double b11, double c11, double a22, double b22, double c22, double a33, double b33, double c33){
        double result1=0;
        double result2=0;
         
        result1 = result1 + (a11*b22*c33) + (b11*c22*a33) + (a22*b33*c11);
        result2 = result2 + (c11*b22*a33) + (a22*b11*c33) + (b33*c22*a11);
        
        
     return (result1- result2);
    
    
    }

