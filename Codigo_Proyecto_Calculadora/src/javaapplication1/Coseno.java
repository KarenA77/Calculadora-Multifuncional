    public String sinpunto(double resultado){    //metodo para que delimite distinga entre enteros y decimales. 
        String retornar="";
  
        retornar=Double.toString(resultado);
        
        if(resultado%1==0){
        retornar=retornar.substring(0,retornar.length()-2);
        }
        
        return retornar;
    }
