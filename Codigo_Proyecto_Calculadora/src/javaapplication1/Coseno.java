    private void calcradActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcradActionPerformed
        //boton para calcular radianes 
     
     
     this.x=Double.parseDouble(this.camporad.getText()); 

        for(int i=0; i<=10; i++){
        
           potencia=1;
           for(int a=0; a<2*i; a++){
              potencia=potencia*x;
             
           }
           factorial=1;
           for(int a=1; a<=2*i; a++){
              factorial=factorial*a; 

           }
           
           if(i%2==0){
             signo=1;
           
           }else{
             signo=-1; 
           } 
           
           
           this.cosenorad =  cosenorad+(potencia/factorial)*signo;
           this.resultado2 = Math.round(this.cosenorad*100);
        }
        
           this.resultrad.setText(sinpunto(this.resultado2/100));

        
        
    }//GEN-LAST:event_calcradActionPerformed

    private void calcgradActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcgradActionPerformed
        // boton para calcular grados
       
    this.x = Double.parseDouble(this.campogrados.getText());
    
    grad=x*0.0175;
    for(int i=0; i<=10; i++){
    
        potencia=1;
        for(int p=0; p<2*i; p++){
        
           potencia=potencia*grad;
        }
        
        factorial=1;
        for(int p=1; p<=2*i; p++){
        
            factorial=factorial*p;
        }
        
        if(i%2==0){
          signo=1;
        
        }else{
          signo=-1;
        }
        
        
         this.cosenograd= cosenograd + (potencia/factorial)*signo;
         this.resultado1=  Math.round(this.cosenograd*100);
     }
    
    
         this.resultgrad.setText(sinpunto(this.resultado1/100));
    }//GEN-LAST:event_calcgradActionPerformed

